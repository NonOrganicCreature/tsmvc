class Position {
    x: number
    y: number
    directionX: number
    directionY: number
    constructor(x: number, y: number, directionX: number, directionY: number) {
        this.x = x
        this.y = y
        this.directionX = directionX
        this.directionY = directionY
    }
    
}

export {
    Position
}